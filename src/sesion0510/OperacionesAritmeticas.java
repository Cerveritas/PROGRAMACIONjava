package sesion0510;

public class OperacionesAritmeticas {

    public static void main(String[] args) {

        // declaracion e inicializacion de las variables
        int a=25, b=5, c=2;
        int suma, resta, multiplicacion, division, resto=0;
        suma=a+b+c;
        resta=c-b;
        division=a/b;
        resto=a%b;
        multiplicacion=a*b;

        System.out.println("la suma es --> "+suma);
        System.out.println("la resta es --> "+resta);
        System.out.println("la multiplicacion es --> "+multiplicacion);
        System.out.println("el cociente de la division es --> "+division);
        System.out.println("el resto de la division es --> "+resto);


        a++;
        System.out.println("el nuevo valor de A es --> "+a);
        b++;
        System.out.println("el nuevo valor de B es --> "+b);
    }














}
