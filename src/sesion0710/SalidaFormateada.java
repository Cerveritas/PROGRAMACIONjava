package sesion0710;

public class SalidaFormateada {

    public static void main(String[] args) {
        System.out.printf("El numero %d no tiene decimales. \n", 21);
       // %d solo admite valores enteros (sin decimales)
        System.out.printf("El numero %f sale con decimales \n", 21.0);
       // %f solo se admiten valores con decimales
        System.out.printf("El %.3f se admiten valores con decimales de exactamente 3 deciamales \n", 21.0 );
      // %.3f se admiten valores con decimales de exactamente 3 deciamales (segun el numero de la f que pongamos)





    }



}
