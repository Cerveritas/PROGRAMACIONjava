package sesion1310;

public class ForIncrementalDecremental {

    public static void main(String[] args) {

    // bucle incremental

        for (int i=1;i<=100;i++)
        {
           System.out.println(i);
        }

    //bucle decremental

        for (int j=100;j>0;j--)
        {
            System.out.println(j);
        }
    }
}
